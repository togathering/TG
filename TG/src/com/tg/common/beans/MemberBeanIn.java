package com.tg.common.beans;

import java.sql.Date;

public class MemberBeanIn {
	private String id;
	private String pass;
	private String nick;
	private String tel;
	private String favo;
	private String loc;
	private String birth;
	private String intro;  
	private String gender;
	private String insta;
	private String fbook;
	private String pic;
	private Date mdate;
	
	public MemberBeanIn() {
	
	}

	public MemberBeanIn(String id, String pass, String nick, String tel,
			String favo, String loc, String birth, String intro, String gender,
			String insta, String fbook, String pic, Date mdate) {
		super();
		this.id = id;
		this.pass = pass;
		this.nick = nick;
		this.tel = tel;
		this.favo = favo;
		this.loc = loc;
		this.birth = birth;
		this.intro = intro;
		this.gender = gender;
		this.insta = insta;
		this.fbook = fbook;
		this.pic = pic;
		this.mdate = mdate;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPass() {
		return pass;
	}

	public void setPass(String pass) {
		this.pass = pass;
	}

	public String getNick() {
		return nick;
	}

	public void setNick(String nick) {
		this.nick = nick;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getFavo() {
		return favo;
	}

	public void setFavo(String favo) {
		this.favo = favo;
	}

	public String getLoc() {
		return loc;
	}

	public void setLoc(String loc) {
		this.loc = loc;
	}

	public String getBirth() {
		return birth;
	}

	public void setBirth(String birth) {
		this.birth = birth;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getInsta() {
		return insta;
	}

	public void setInsta(String insta) {
		this.insta = insta;
	}

	public String getFbook() {
		return fbook;
	}

	public void setFbook(String fbook) {
		this.fbook = fbook;
	}

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}

	public Date getMdate() {
		return mdate;
	}

	public void setMdate(Date mdate) {
		this.mdate = mdate;
	}
}

	